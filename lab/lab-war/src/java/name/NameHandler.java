package name;

/**
 *
 * @author camilo
 */
public class NameHandler {
    private String date;
    private String name;
    private String age;

    public NameHandler() {
        this.date = " " ;
        this.name = " " ;
        this.age =  " " ;
    }

    public String getDate() {
        return date;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(String age) {
        this.age = age;
    }

    



  
    
    
}
